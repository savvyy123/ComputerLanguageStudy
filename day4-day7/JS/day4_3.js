// day = 3 として、switch文で曜日を表示してください。
// 1→"月曜日" 2→"火曜日" 3→"水曜日" それ以外→"その他"
// 期待される出力：水曜日

const day = 3;

// ここに書いてね
switch (day) {
  case 1:
    console.log("月曜日");
    break;
  case 2:
    console.log("火曜日");
    break;
  case 3:
    console.log("水曜日");
    break;
  default:
    console.log("その他");
}